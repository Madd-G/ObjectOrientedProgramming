public class Jok {
    private final String bahan;
    private final String ukuran;

    public Jok(String bahan, String ukuran) {
        this.bahan = bahan;
        this.ukuran = ukuran;
    }

    public String getBahan() {
        return bahan;
    }

    public String getUkuran() {
        return ukuran;
    }
}
