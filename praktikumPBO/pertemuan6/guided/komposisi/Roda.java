package alamsyah.praktikumPBO.pertemuan6.guided.komposisi;

public class Roda {
    private final String merk;
    private final String ring;

    public Roda(String merk, String ring) {
        this.merk = merk;
        this.ring = ring;
    }

    public String getMerk() {
        return merk;
    }

    public String getRing() {
        return ring;
    }

}
