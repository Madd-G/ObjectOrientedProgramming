package alamsyah.praktikumPBO.pertemuan6.guided.komposisi;

public class Roda {
    private String merk;
    private String ring;

    public Roda(String merk, String ring) {
        this.merk = merk;
        this.ring = ring;
    }

    public String getMerk() {
        return merk;
    }

    public void setMerk(String merk) {
        this.merk = merk;
    }

    public String getRing() {
        return ring;
    }

    public void setRing(String ring) {
        this.ring = ring;
    }
}
